pub fn get_league_live_data<T: for<'de> serde::Deserialize<'de>>(path: &str) -> Option<T> {
    let url = format!("{}{}", "https://127.0.0.1:2999/liveclientdata", path);
    let certificate = Certificate::from_pem(RIOT_CERTIFICATE).unwrap();
    let client = reqwest::blocking::Client::builder()
        .add_root_certificate(certificate)
        .build()
        .unwrap();
    match client.get(url).send() {
        Err(_) => None,
        Ok(res) => res.json::<T>().ok().or_else(|| {
            println!("Parsing JSON error!");
            None
        }),
    }
}
